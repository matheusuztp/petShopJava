    private void carregaAnimais() {
        BDAnimal BDAnimal = new BDAnimal();
        ArrayList<Animal> animais = BDAnimal.selectAll();
        
        DefaultTableModel tabelaModelo =  (DefaultTableModel) getjTable1().getModel();
        tabelaModelo.setNumRows(0);
        
        for(Animal animal : animais)
        {
            tabelaModelo.addRow(new Object[]{
                animal.getRegistro(),
                animal.getNome(),
                animal.getTipo(),
                animal.getRaca(),
                animal.getCor(),
                animal.getCliente().getNome()
            });
        }
    }
